int	ft_isalnum(int symb)
{
	if ((symb >= '0' && symb <= '9')
		|| (symb >= 'A' && symb <= 'Z')
		|| (symb >= 'a' && symb <= 'z'))
	{
		return (1);
	}
	return (0);
}
