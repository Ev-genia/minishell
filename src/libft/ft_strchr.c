char	*ft_strchr(const char *str, int symb)
{
	int	i;

	i = 0;
	while (str[i] != (unsigned char)symb)
	{
		if (str[i] == '\0')
			return ((void *)0);
		i++;
	}
	return ((char *)&str[i]);
}
